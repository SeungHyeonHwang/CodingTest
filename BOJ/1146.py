def solution(n):

	dp = [0]*(n+1)
	dp[1] = 1
	dp[2] = 2
	if n < 3 : return dp[n]

	for i in range(3,(n+1)):
		if i%2 == 0 :
			dp[i] = dp[i-2]*(5*int((i-1)/2))
		elif i%2 == 1 : 
			dp[i] = dp[i-2]*(4*int(i/2))
		
	return dp[n]%int(1e6)

n = int(input())
print(solution(n))
